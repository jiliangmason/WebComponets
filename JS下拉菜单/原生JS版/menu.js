    function showTag(li) {
        var oul_display = li.getElementsByTagName('ul')[0];
        oul_display.style.display = "block";
    }

    function  hideTag(li) {
        var oul_hidden = li.getElementsByTagName('ul')[0];
        oul_hidden.style.display = "none";
    }





